export default function SocialLinksRow({ links = {}, size = "default" }) {
  const sizeClasses = {
    default: "w-10 h-10",
    sm: "w-8 h-8",
    lg: "w-12 h-12",
  };

  const iconSizes = {
    default: "w-5 h-5",
    sm: "w-4 h-4",
    lg: "w-6 h-6",
  };

  const platformOrder = ['spotify', 'appleMusic', 'youtube', 'instagram', 'tiktok', 'soundcloud', 'website'];
  
  const availableLinks = platformOrder.filter(platform => links[platform]);

  if (availableLinks.length === 0) return null;

  return (
    <div className="flex items-center gap-2 flex-wrap">
      {availableLinks.map(platform => {
        const Icon = platformIcons[platform];
        const url = links[platform];
        
        return (
          <a
            key={platform}
            href={url}
            target="_blank"
            rel="noopener noreferrer"
            className={`${sizeClasses[size]} bg-zinc-800/80 hover:bg-zinc-700 rounded-full flex items-center justify-center transition-all hover:scale-105`}
            title={platform.charAt(0).toUpperCase() + platform.slice(1)}
          >
            <Icon className={`${iconSizes[size]} text-zinc-300`} />
          </a>
        );
      })}
    </div>
  );
}